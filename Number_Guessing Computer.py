def main():
    # Step 1: Prompt the user for the first number
    num1 = int(input("Enter the first number: "))  # Pehla number maango aur integer mein badlo
    
    # Step 2: Prompt the user for the second number
    num2 = int(input("Enter the second number: "))  # Doosra number maango aur integer mein badlo
    
    # Step 3: Calculate the sum of the two numbers
    total = num1 + num2  # Dono numbers ka sum calculate karo
    
    # Step 4: Print the result with a message
    print(f"The sum of {num1} and {num2} is {total}.")  # Result user ko dikhayenge

# Call the main function
main()




